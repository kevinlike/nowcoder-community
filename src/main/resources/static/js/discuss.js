$(function(){
    $("#topBtn").click(setTop);
    $("#wonderfulBtn").click(setWonderful);
    $("#deleteBtn").click(setDelete);
});

function like(btn,entityType,entityId,entityUserId,postId){
    $.post(
        CONTEXT_PATH+"/like",
        {"entityType":entityType,"entityId":entityId,"entityUserId":entityUserId,"postId":postId},
        function(data){
            data=$.parseJSON(data);
            if(data.code==0){
                if(data.likeStatus==1)
                {
                    $(btn).children("b").text("已赞");
                }
                else if(data.likeStatus==0){
                    $(btn).children("b").text("赞");
                }
                $(btn).children("i").text(data.likeCount);
            }
            else{
                alert(data.msg);
            }
        }
    );
}

//置顶
function setTop(){
    $.post(
        CONTEXT_PATH+"/discuss/top",
        {
            "id":$("#postId").val()
        },
        function(data){
            data=$.parseJSON(data);
            if(data.code==0){
                //已经置顶后，置顶按钮就不可用了
                $("#topBtn").attr("disabled","disabled");
            }
            else{
                alert(data.msg);
            }
        }
    )
}
//加精
function setWonderful(){
    $.post(
        CONTEXT_PATH+"/discuss/wonderful",
        {
            "id":$("#postId").val()
        },
        function(data){
            data=$.parseJSON(data);
            if(data.code==0){
                //已经加精后，加精按钮就不可用了
                $("#wonderfulBtn").attr("disabled","disabled");
            }
            else{
                alert(data.msg);
            }
        }
    )
}
//删除
function setDelete(){
    $.post(
        CONTEXT_PATH+"/discuss/delete",
        {
            "id":$("#postId").val()
        },
        function(data){
            data=$.parseJSON(data);
            if(data.code==0){
                //在提示框显示删除提示
                alert("已删除");
                setTimeout(function(){
                    //跳转页面到首页
                    if(data.code==0){
                        window.location.href=CONTEXT_PATH+"/index";
                    }
                }, 2000);
            }
            else{
                alert(data.msg);
            }
        }
    )
}
